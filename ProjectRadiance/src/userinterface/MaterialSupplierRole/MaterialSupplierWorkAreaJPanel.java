    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        else{
            MaterialWorkRequest request = (MaterialWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
            MaterialSupplierViewJPanel panel = new MaterialSupplierViewJPanel(userProcessContainer, request);
            userProcessContainer.add("MaterialSupplierViewJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

